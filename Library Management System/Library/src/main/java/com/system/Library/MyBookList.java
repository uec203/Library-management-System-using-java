    public MyBookList(int UID) {
        initComponents();
        setVisible(true);
        try{
            Connection con = Connect.connect();
            Statement stmt = con.createStatement();
            ResultSet rs=stmt.executeQuery(("select * from issued where UID="+UID));
            ResultSetMetaData rm = rs.getMetaData();
            String title[] = new String[rm.getColumnCount()];
            for(int i=0;i<rm.getColumnCount();i++){
                title[i]=(String)rm.getColumnName(i+1);
                }
            int count =0;
            while(rs.next()) count++;
            rs=stmt.executeQuery(("select * from issued where UID="+UID));
            String data[][] = new String[count][rm.getColumnCount()];
            count=0;
            while(rs.next()){
                for(int i=0;i<rm.getColumnCount();i++){
                    data[count][i]=rs.getString(i+1);
                    
                }
                count++;
            }
            JTable ti = new JTable(data,title);
            jScrollPane1.setViewportView(ti);
        }
        catch(Exception e){
            System.out.print(e);
        }
        
    }

